let myAge = 25;    //first declaration of a variable
console.log(myAge); // Result is 25 in the console.
myAge = 26;  // reassigned value of a variable
console.log(myAge); // Result is 26 in the console.

const myName = "Ali";    //first declaration of a constant
console.log(myName); // Result is “Ali” in the console.
myName = "Ahmet";  // This reassigning a constant will give an error.
console.log(myName); // Will not give a result. Program will stop when the above error occurs.